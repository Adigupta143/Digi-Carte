    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SessionFactory sf1 =new Configuration().configure().buildSessionFactory();
        Session session = sf1.openSession();
        Transaction tx = session.beginTransaction();
        String id=jTextField1.getText();
        String name=jTextField2.getText();
        char ch[] = jPasswordField1.getPassword();
        String pass = new String(ch);
        String section = jTextField3.getText();
        String purpose = (String)jComboBox1.getSelectedItem();
        Chef chef = new Chef(id, name, pass, section, purpose);
        session.save(chef);
        tx.commit();
        session.close();
        JOptionPane.showMessageDialog(this,"account has been created");
    }//GEN-LAST:event_jButton1ActionPerformed

