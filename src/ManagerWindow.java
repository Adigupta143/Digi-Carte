    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
SessionFactory sf1 =new Configuration().configure().buildSessionFactory();
        Session session = sf1.openSession();
        Transaction tx = session.beginTransaction();
        String id = jTextField4.getText();
        String oPass =new String(jPasswordField2.getPassword());
        String nPass =new String(jPasswordField3.getPassword());
        String cnfPass =new String(jPasswordField4.getPassword());

        if(nPass.equals(cnfPass))
        {
            Manager man = (Manager)session.get(Manager.class,id); //primarykey
            man.setPassword(nPass);
            session.update(man);
            tx.commit();
            session.close();
            JOptionPane.showMessageDialog(this,"PASSWORD CHANGED SUCCESSFULLY");
            new NewWindow().setVisible(true);
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "new password and confirmed doesnot match");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

