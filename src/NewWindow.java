    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        SessionFactory sf1 =new Configuration().configure().buildSessionFactory();
        Session session = sf1.openSession();
        Transaction tx = session.beginTransaction();
         id=jTextField2.getText();
        String name=jTextField3.getText();
        char ch[] = jPasswordField2.getPassword();
        String pass = new String(ch);
        String email=jTextField4.getText();
        int contact=Integer.parseInt(jTextField5.getText());
        String address=jTextField6.getText();
        String purpose=jTextField7.getText();
        Customer customer = new Customer(id, name, pass, email, contact,address,purpose);
        session.save(customer);
        tx.commit();
        session.close();
        JOptionPane.showMessageDialog(this,"account has been created");
    }//GEN-LAST:event_jButton2ActionPerformed

