    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         id=jTextField1.getText();
        char ch[]=jPasswordField1.getPassword();
        String pass=new String(ch);
        String post=(String)jComboBox1.getSelectedItem();
        
        if(id.equals(""))
        {
            jLabel3.setText("Id is required!");
        }
        else if(pass.equals(""))
        {
            jLabel3.setText("Password is required!");
        }
        else if(post.equals("SELECT POST"))
        {
            jLabel3.setText("Please select valid post");
        }
        else
        {
            if(post.equalsIgnoreCase("customer"))
            {
                SessionFactory sf=
                        new Configuration().configure().buildSessionFactory();
              Session session=sf.openSession();
              Transaction tx=session.beginTransaction();
              Criteria ct=session.createCriteria(Customer.class);
              ct.add(Restrictions.and(Restrictions.eq(
              "custId",id),Restrictions.eq("pass",pass)));
              List<Customer> list = ct.list();
              
              if(list.isEmpty())
              {
                  jLabel3.setText("Invalid id/password");
              }
              else
              {
                  new CustomerWindow().setVisible(true);
                  dispose();
              }
            }
            if(post.equalsIgnoreCase("manager"))
            {
                SessionFactory sf=
                        new Configuration().configure().buildSessionFactory();
              Session session=sf.openSession();
              Transaction tx=session.beginTransaction();
              Criteria ct=session.createCriteria(Manager.class);
              ct.add(Restrictions.and(Restrictions.eq(
              "managerId",id),Restrictions.eq("password",pass)));
              List<Manager> list = ct.list();
              
              if(list.isEmpty())
              {
                  jLabel3.setText("Invalid id/password");
              }
              else
              {
                  new ManagerWindow().setVisible(true);
                  dispose();
              }
            }
            if(post.equalsIgnoreCase("chef"))
            {
                SessionFactory sf=
                        new Configuration().configure().buildSessionFactory();
              Session session=sf.openSession();
              Transaction tx=session.beginTransaction();
              Criteria ct=session.createCriteria(Chef.class);
              ct.add(Restrictions.and(Restrictions.eq(
              "chefId",id),Restrictions.eq("pass",pass)));
              List<Chef> list = ct.list();
              
              if(list.isEmpty())
              {
                  jLabel3.setText("Invalid id/password");
              }
              else
              {
                  new ChefWindow().setVisible(true);
                  dispose();
              }
            }
              
                  
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

